package ex_nine;

public class Item {
	private String name;
	private int price;
	private int wight;
	
	public void setName(String n) {
		this.name = n;
	}
	public void setPrice(int p) {
		this.price = p;
	}
	public void setWeight(int w) {
		this.wight = w;
	}
	public String getName() {
		return this.name;
	}
	public int getPrice() {
		return this.price;
	}
	public int getWeight() {
		return this.wight;
	}
}
